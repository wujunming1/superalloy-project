public class Reference {
    private String citationJournalIdAstm;
    private int citationYear;
    private String citationId;
    private int citationJournalVolume;
    private int citationPageFirst;
    private String citationJournalAbbrev;
    private int citationPageLast;

    public String getCitationJournalIdAstm() {
        return citationJournalIdAstm;
    }

    public void setCitationJournalIdAstm(String citationJournalIdAstm) {
        this.citationJournalIdAstm = citationJournalIdAstm;
    }

    public int getCitationYear() {
        return citationYear;
    }

    public void setCitationYear(int citationYear) {
        this.citationYear = citationYear;
    }

    public String getCitationId() {
        return citationId;
    }

    public void setCitationId(String citationId) {
        this.citationId = citationId;
    }

    public int getCitationJournalVolume() {
        return citationJournalVolume;
    }

    public void setCitationJournalVolume(int citationJournalVolume) {
        this.citationJournalVolume = citationJournalVolume;
    }

    public int getCitationPageFirst() {
        return citationPageFirst;
    }

    public void setCitationPageFirst(int citationPageFirst) {
        this.citationPageFirst = citationPageFirst;
    }

    public String getCitationJournalAbbrev() {
        return citationJournalAbbrev;
    }

    public void setCitationJournalAbbrev(String citationJournalAbbrev) {
        this.citationJournalAbbrev = citationJournalAbbrev;
    }

    public int getCitationPageLast() {
        return citationPageLast;
    }

    public void setCitationPageLast(int citationPageLast) {
        this.citationPageLast = citationPageLast;
    }
}
