public class dataInfo {
    private String dataAbstract;
    private String dataProducer;
    private String dataChecker;
    private String dataFrom;

    public String getDataAbstract() {
        return dataAbstract;
    }

    public void setDataAbstract(String dataAbstract) {
        this.dataAbstract = dataAbstract;
    }

    public String getDataProducer() {
        return dataProducer;
    }

    public void setDataProducer(String dataProducer) {
        this.dataProducer = dataProducer;
    }

    public String getDataChecker() {
        return dataChecker;
    }

    public void setDataChecker(String dataChecker) {
        this.dataChecker = dataChecker;
    }

    public String getDataFrom() {
        return dataFrom;
    }

    public void setDataFrom(String dataFrom) {
        this.dataFrom = dataFrom;
    }
}
