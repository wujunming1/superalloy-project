package entity;

import java.util.Date;

public class AlgorithmEntity {
    private int id;
    private String algoName;
    private String algoLocation;
    private String algoAbstract;
    private String algoDate;
    private String algoSubmissionName;
    private String algoType;
    private String calField;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAlgoName() {
        return algoName;
    }

    public void setAlgoName(String algoName) {
        this.algoName = algoName;
    }

    public String getAlgoLocation() {
        return algoLocation;
    }

    public void setAlgoLocation(String algoLocation) {
        this.algoLocation = algoLocation;
    }

    public String getAlgoAbstract() {
        return algoAbstract;
    }

    public void setAlgoAbstract(String algoAbstract) {
        this.algoAbstract = algoAbstract;
    }

    public String getAlgoDate() {
        return algoDate;
    }

    public void setAlgoDate(String algoDate) {
        this.algoDate = algoDate;
    }

    public String getAlgoSubmissionName() {
        return algoSubmissionName;
    }

    public void setAlgoSubmissionName(String algoSubmissionName) {
        this.algoSubmissionName = algoSubmissionName;
    }

    public String getAlgoType() {
        return algoType;
    }

    public void setAlgoType(String algoType) {
        this.algoType = algoType;
    }

    public String getCalField() {
        return calField;
    }

    public void setCalField(String calField) {
        this.calField = calField;
    }
}
